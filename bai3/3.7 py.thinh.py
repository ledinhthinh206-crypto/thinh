def checkValue(n):
    # Kiểm tra số dư khi chia cho 2
    if n % 2 == 0:
        print("Đây là một số chẵn")
    else:
        print("Đây là một số lẻ")

# Ví dụ 1: Kiểm tra số 7
print("Kiểm tra số 7:")
checkValue(7)

# Ví dụ 2: Kiểm tra số 10
print("\nKiểm tra số 10:")
checkValue(10)
